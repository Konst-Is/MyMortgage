import UIKit

extension UIColor {
    
    static var deepBlue: Self {
        return createFrom(red: 37, green: 19, blue: 221, alpha: 1)
    }
    
    static var middleBlue: Self {
        return createFrom(red: 74, green: 119, blue: 229, alpha: 1)
    }
    
    static var lightBlue: Self {
        return createFrom(red: 110, green: 146, blue: 233, alpha: 1)
    }
    
}
